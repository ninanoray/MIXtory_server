public class Recipes {
    private int rcp_id;
    private String rcp_cName;
    private String rcp_igdCategory;
    private int rcp_amount;

    public int getRcp_id() {
        return rcp_id;
    }

    public void setRcp_id(int rcp_id) {
        this.rcp_id = rcp_id;
    }

    public String getRcp_cName() {
        return rcp_cName;
    }

    public void setRcp_cName(String rcp_cName) {
        this.rcp_cName = rcp_cName;
    }

    public String getRcp_igdCategory() {
        return rcp_igdCategory;
    }

    public void setRcp_igdCategory(String rcp_igdCategory) {
        this.rcp_igdCategory = rcp_igdCategory;
    }

    public int getRcp_amount() {
        return rcp_amount;
    }

    public void setRcp_amount(int rcp_amount) {
        this.rcp_amount = rcp_amount;
    }
}
