package com.MIXtory.demo;

public class Ingredient {
    private int igd_id;
    private String igd_name;
    private String igd_type;
    private String igd_category;
    private String igd_url;

    public int getIgd_id() {
        return igd_id;
    }

    public void setIgd_id(int igd_id) {
        this.igd_id = igd_id;
    }

    public String getIgd_name() {
        return igd_name;
    }

    public void setIgd_name(String igd_name) {
        this.igd_name = igd_name;
    }

    public String getIgd_type() {
        return igd_type;
    }

    public void setIgd_type(String igd_type) {
        this.igd_type = igd_type;
    }

    public String getIgd_category() {
        return igd_category;
    }

    public void setIgd_category(String igd_category) {
        this.igd_category = igd_category;
    }

    public String getIgd_url() {
        return igd_url;
    }

    public void setIgd_url(String igd_url) {
        this.igd_url = igd_url;
    }
}
